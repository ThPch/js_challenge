const incrementMyBoard = (type, board, x, y) => {
    switch (type) {
        case 0 : 
            board[x][y] = board[x][y] + 1
            break;
        case 1 : 
            y[0] < y[1] ? incrementByAxe(axe = 'y', board, x, [y[0], y[1]]) : incrementByAxe(axe = 'y', board, x,  [y[1], y[0]])
        case 2 : 
            x[0] < x[1] ? incrementByAxe(axe = 'x', board, [x[0], x[1]], y) : incrementByAxe(axe = 'x', board, [x[1], x[0]],  y)
    }       

}

const incrementByAxe = (axe, board, x, y)  => {
    switch (axe) {
        case 'y' : 
            for(let yAxis = y[0]; yAxis <= y[1] ; yAxis++) {
                board[x][yAxis] = board[x][yAxis] + 1
            }
            break;
        case 'x' :
            for(let xAxis = x[0]; xAxis <= x[1] ; xAxis++) {
                board[xAxis][y] = board[xAxis][y] + 1
            }
            break;
    }  
}

